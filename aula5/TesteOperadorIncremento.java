public class TesteOperadorIncremento { 

	public static void main(String args[]) {
	
	int contador = 10;
	System.out.println("contador ==>" + contador);
	contador ++;
	System.out.println("contador ==>" + contador);
	contador ++;
	System.out.println("contador ==>" + contador);
	contador *= 2;
	System.out.println("contador ==>" + contador);



    }

}